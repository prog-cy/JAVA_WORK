
public class AbsDemo {

	public static void main(String[] args) {
		
		Math m = new Math();
		
		System.out.println("absolut value of -10: "+m.abs(-10));
		
		System.out.println("absolute value of -23.456: "+m.abs(-23.456));
		
		System.out.println("absolut value of -13: "+m.abs(-13l));
		
		System.out.println("absolute value of -3.4: "+m.abs(-3.4f));

	}

}
